#include "SoundManager.h"

#include "SE.h"

#define SE_PATH "Material/Sound/SE/"

/*=============================================================================================
	コンストラクタ
=============================================================================================*/
SE::SE()
	:SoundInterface()
{
}

/*=============================================================================================
	デストラクタ
=============================================================================================*/
SE::~SE()
{
}

/*=============================================================================================
	読み込み処理
=============================================================================================*/
void SE::Load(const char* fileName)
{
	m_soundID = SoundManager::Load(SE_PATH, fileName);
}

/*=============================================================================================
	リバーブの設定処理
=============================================================================================*/
void SE::SetReverb(const char* fileName)
{
	SoundManager::SetReverb(m_soundID, fileName);
}

/*=============================================================================================
	再生処理
=============================================================================================*/
void SE::Play(int loopCount)
{
	this->Stop();
	SoundManager::Play(m_soundID, loopCount);
}

/*=============================================================================================
	一時停止処理
=============================================================================================*/
void SE::Pause()
{
	SoundManager::Pause(m_soundID);
}

/*=============================================================================================
	停止処理
=============================================================================================*/
void SE::Stop()
{
	SoundManager::Stop(m_soundID);
}

/*=============================================================================================
	音量調節処理
=============================================================================================*/
void SE::SetVolume(int volume)
{
	SoundManager::SetVolume(m_soundID, volume);
}

/*=============================================================================================
	パンニング調節処理
=============================================================================================*/
void SE::SetPanning(int left, int right, float degree)
{
	SoundManager::SetPanning(m_soundID, left, right, degree);
}
