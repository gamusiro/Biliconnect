#include "SoundManager.h"

#include "BGM.h"

#define BGM_PATH "Material/Sound/BGM/"

/*=============================================================================================
	コンストラクタ
=============================================================================================*/
BGM::BGM()
	:SoundInterface()
{
}

/*=============================================================================================
	デストラクタ
=============================================================================================*/
BGM::~BGM()
{
}

/*=============================================================================================
	読み込み処理
=============================================================================================*/
void BGM::Load(const char* fileName)
{
	max(0, 0);
	m_soundID = SoundManager::Load(BGM_PATH, fileName);
}

/*=============================================================================================
	再生処理
=============================================================================================*/
void BGM::Play(int loopCount)
{
	SoundManager::Play(m_soundID, loopCount);
}

/*=============================================================================================
	一時停止処理
=============================================================================================*/
void BGM::Pause()
{
	SoundManager::Pause(m_soundID);
}

/*=============================================================================================
	停止処理
=============================================================================================*/
void BGM::Stop()
{
	SoundManager::Stop(m_soundID);
}

/*=============================================================================================
	音量調節処理
=============================================================================================*/
void BGM::SetVolume(int volume)
{
	SoundManager::SetVolume(m_soundID, volume);
}

/*=============================================================================================
	パンニング設定処理
=============================================================================================*/
void BGM::SetPanning(int left, int right, float degree)
{
	SoundManager::SetPanning(m_soundID, left, right, degree);
}
